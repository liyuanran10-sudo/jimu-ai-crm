export async function readCrmDb() {
  const blobDb = await readCrmDbFromBlobStore();
  if (blobDb) return normalizeDb(blobDb);

  const dbPath = getDbPath();
  try {
    const raw = await fs.readFile(dbPath, "utf8");
    return normalizeDb(JSON.parse(raw));
  } catch (error) {
    if (error.code !== "ENOENT") throw error;
    const db = createDefaultDb();
    await writeCrmDb(db);
    return db;
  }
}

export async function writeCrmDb(db) {
  const normalized = normalizeDb(db);
  await writeCrmDbToBlobStore(normalized);

  const dbPath = getDbPath();
  await fs.mkdir(path.dirname(dbPath), { recursive: true });
  const tempPath = `${dbPath}.${process.pid}.${Date.now()}.${crypto.randomUUID()}.tmp`;
  await fs.writeFile(tempPath, `${JSON.stringify(normalized, null, 2)}\n`, "utf8");
  await fs.rename(tempPath, dbPath);
  return normalized;
}

async function readCrmDbFromBlobStore() {
  const store = await getCrmBlobStore();
  if (!store) return null;
  try {
    return await store.getJSON("crm-db");
  } catch {
    return null;
  }
}

async function writeCrmDbToBlobStore(db) {
  const store = await getCrmBlobStore();
  if (!store) return false;
  await store.setJSON("crm-db", db);
  return true;
}

async function getCrmBlobStore() {
  if (!process.env.NETLIFY || process.env.NETLIFY_BLOBS_DISABLED === "true") return null;
  try {
    const { getStore } = await import("@netlify/blobs");
    return getStore({ name: "aicrm", consistency: "strong" });
  } catch {
    return null;
  }
}

let crmDbWriteQueue = Promise.resolve();

export async function withCrmDb(mutator) {
  const operation = crmDbWriteQueue.then(async () => {
    const db = await readCrmDb();
    const result = await mutator(db);
    await writeCrmDb(db);
    return result;
  });
  crmDbWriteQueue = operation.catch(() => {});
  return operation;
}

