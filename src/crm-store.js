export function normalizeItem(collection, item = {}) {
  const base = mapLegacyFields(collection, item);
  const id = base.id || `${singular(collection)}_${crypto.randomUUID()}`;
  const common = { ...base, id };

  if (collection === "users") {
    return normalizeUser(common);
  }
  if (collection === "models") {
    return normalizeModel(common);
  }
  if (collection === "stages") {
    return {
      ...common,
      name: clean(common.name),
      enabled: common.enabled !== false,
      sortOrder: Number(common.sortOrder || 999)
    };
  }
  if (collection === "skills") {
    return {
      ...common,
      name: clean(common.name),
      applicableStages: ensureArray(common.applicableStages),
      inputFields: ensureArray(common.inputFields),
      knowledgeBaseIds: ensureArray(common.knowledgeBaseIds),
      status: common.status || "enabled"
    };
  }
  if (collection === "knowledgeBases") {
    const { uploadedDocuments, uploadedDocumentsJson, ...rest } = common;
    const parsedUploads = parseUploadedDocuments(uploadedDocuments || uploadedDocumentsJson);
    return {
      ...rest,
      documents: normalizeKnowledgeBaseDocuments(common.documents, parsedUploads),
      status: common.status || "enabled",
      updatedAt: nowIso()
    };
  }
  if (collection === "customers") {
    return {
      ...common,
      name: clean(common.name),
      status: common.status || "跟进中",
      estimatedAmount: Number(common.estimatedAmount || 0)
    };
  }
  if (collection === "customerMemories") {
    return {
      ...common,
      customerId: clean(common.customerId),
      userId: clean(common.userId),
      memoryType: clean(common.memoryType || "customer_memory"),
      strategy: clean(common.strategy),
      title: clean(common.title || "客户记忆"),
      content: clean(common.content),
      sourceType: clean(common.sourceType || "ai_generation"),
      sourceRecordId: clean(common.sourceRecordId),
      status: common.status || "active"
    };
  }
  if (collection === "reportFeedbacks") {
    return {
      ...common,
      customerId: clean(common.customerId),
      customerName: clean(common.customerName),
      recordId: clean(common.recordId),
      recordTitle: clean(common.recordTitle),
      generationType: clean(common.generationType),
      userId: clean(common.userId),
      userName: clean(common.userName),
      feedbackContent: clean(common.feedbackContent),
      originalContentPreview: clean(common.originalContentPreview),
      aiOptimizationSuggestion: clean(common.aiOptimizationSuggestion),
      status: common.status || "generating"
    };
  }
  return common;
}

export function hashPassword(password = "") {
  return crypto.createHash("sha256").update(String(password)).digest("hex");
}

export function nowIso() {
  return new Date().toISOString();
}

