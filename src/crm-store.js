function singular(collection) {
  return collection.replace(/ies$/, "y").replace(/s$/, "");
}

function getDbPath() {
  const baseDir = getWritableBaseDir();
  return path.join(baseDir, CRM_DB_FILE);
}

function getWritableBaseDir() {
  const cwd = process.cwd();
  if (
    process.env.NETLIFY
    || process.env.AWS_LAMBDA_FUNCTION_NAME
    || process.env.AWS_EXECUTION_ENV
    || cwd.includes("/var/task")
  ) {
    return "/tmp";
  }
  return path.resolve("data");
}

function clean(value) {
  return String(value || "").trim();
}

function ensureArray(value) {
  if (Array.isArray(value)) return value;
  if (typeof value === "string" && value.trim()) {
    return value.split(/[,，\n]/).map((item) => item.trim()).filter(Boolean);
  }
  return [];
}

function parseUploadedDocuments(value) {
  if (!value) return [];
  if (Array.isArray(value)) return value;
  if (typeof value !== "string") return [value];
  try {
    const parsed = JSON.parse(value);
    return Array.isArray(parsed) ? parsed : [];
  } catch {
    return [];
  }
}

function nextDate(offsetDays) {
  const date = new Date();
  date.setDate(date.getDate() + offsetDays);
  return date.toISOString();
}
